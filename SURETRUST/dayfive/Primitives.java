package dayfive;

public class Primitives {
    public static void main(String[] args) {
        //numbers
        byte a1=12;
        short b1=522;
        int c1=1331;
        long d1=234567;
        System.out.println(a1);
        System.out.println(b1);
        System.out.println(c1);
        System.out.println(d1);

        //floating points
        float e1=1.123456789123456789f;//suffix f is mandatory, precision upto 8 digits.
        double f1=1.123456789123456789;//suffix d is optional, precision upto 17 digits.
        System.out.println(e1);
        System.out.println(f1);

        //character
        char g1='w';
        System.out.println(g1);

        //boolean
        boolean h1=false;
        System.out.println(h1);

        
    }
    
}
